/**
 * generate-content-ledger.mjs — Content Factory measurement ledger (Factory#1949 Phase 2.5).
 *
 * Builds the append-only content ledger for the focusbro /guides/ surfaces the
 * Content Factory tracks in this repo (coordination issue focusbro#6, design doc
 * Factory `docs/planning/content-factory.md` §7).
 *
 * Records, for every published guide, the six §7 fields —
 *   URL · target keyword · content type · publish date · spine pointer · request id
 * plus whether the guide's in-app CTA carries a `?ref=cf_focusbro_*` attribution param.
 *
 * Derived deterministically from the guides module of record —
 * `api/src/guides/index.js` (the array the Worker server-renders every
 * `/guides/<slug>.html` page and `/sitemap.xml` from) — so the ledger can never
 * drift from what is actually live. Re-running it is a no-op unless a guide changed.
 *
 * Run:   node scripts/generate-content-ledger.mjs           # write docs/content-ledger.md
 *        node scripts/generate-content-ledger.mjs --check    # CI: fail if the committed
 *                                                            #     ledger is stale
 */

import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const GUIDES_MODULE = join(ROOT, 'api', 'src', 'guides', 'index.js');
const LEDGER_PATH = join(ROOT, 'docs', 'content-ledger.md');

const CONTENT_TYPE = 'guide';
// Every focusbro guide derives from the app design spine's content-layer
// requirement (R-007) — the product truth governing what a guide may claim.
const SPINE_POINTER = 'docs/REQUIREMENTS.md (R-007)';

/** Natural (numeric-aware) comparison so slugs sort stably. */
function naturalCompare(a, b) {
  return a.localeCompare(b, 'en', { numeric: true, sensitivity: 'base' });
}

/** Load the exported guides array from the module of record. */
async function loadGuides() {
  const mod = await import(pathToFileURL(GUIDES_MODULE).href);
  const guides = mod.guides ?? mod.default;
  if (!Array.isArray(guides)) {
    throw new Error('guides module did not export a `guides` array');
  }
  return guides;
}

/** Turn one guide object into a ledger row. */
function toRow(guide) {
  const slug = guide.slug;
  const url = `https://focusbro.net/guides/${slug}.html`;
  // focusbro guides declare no `<meta name="keywords">`; the URL slug IS the
  // topical target, so derive the keyword from it deterministically.
  const targetKeyword = slug.replace(/-/g, ' ');
  // Attribution: the §7 convention is a CTA `?ref=cf_<app>_<slug>`. focusbro's
  // guides-deeplink test enforces a BARE `/?tool=<id>` CTA, so no guide can carry
  // `?ref` today — detect it honestly so the ledger reflects reality (and picks up
  // any future handler-side attribution the moment it lands).
  const instrumented = /ref=cf_focusbro_/.test(guide.body || '');
  return {
    slug,
    url,
    targetKeyword,
    contentType: CONTENT_TYPE,
    publishDate: guide.lastmod || '',
    spine: SPINE_POINTER,
    requestId: `cf_focusbro_${slug}`,
    instrumented,
  };
}

/** Render the ledger Markdown from the collected rows. */
function renderLedger(rows) {
  const total = rows.length;
  const instrumented = rows.filter((r) => r.instrumented).length;
  const gap = total - instrumented;

  const header = [
    '<!-- GENERATED by scripts/generate-content-ledger.mjs — do not edit by hand. -->',
    '<!-- Re-run: node scripts/generate-content-ledger.mjs -->',
    '',
    '# Content Ledger — focusbro /guides/ surfaces',
    '',
    'The append-only measurement record for every guide the **Content Factory**',
    '(Factory#1949, Wave 3) tracks in this repo. Coordination issue **focusbro#6**.',
    'Spec: Factory `docs/planning/content-factory.md` §7 (Measurement) — *"Content live ≠',
    'content working. Every piece ships instrumented so indexing and traffic can be',
    'audited later."*',
    '',
    'Derived deterministically from the guides module of record —',
    '`api/src/guides/index.js` (the array the Worker server-renders every',
    '`/guides/<slug>.html` page and the `/sitemap.xml` from) — so it can never drift',
    'from what is actually live. Regenerate with `node scripts/generate-content-ledger.mjs`',
    '(`--check` fails CI if the committed ledger is stale).',
    '',
    `**Totals:** ${total} guides · ${instrumented} carry a \`?ref=cf_focusbro_*\` CTA ·`,
    `**${gap} not yet instrumented** (see the \`ref\` column + the attribution note below).`,
    '',
    '## Attribution note (§7 portability gap)',
    '',
    "The §7 convention is a CTA `?ref=cf_<app>_<slug>` deep-link. focusbro's",
    '`api/src/__tests__/guides-deeplink.test.js` pins every guide CTA to a **bare**',
    '`/?tool=<id>` link (regex `^/\\?tool=<id>$`), so a guide cannot append `?ref=`',
    'without failing that gate. Until focusbro adds handler-side param passthrough',
    "(or credits via this ledger's request id), the `ref` column stays `—` for every",
    'guide and the **request id is the attribution key** rather than a CTA param.',
    'This is the CTA-ref portability caveat logged on Factory#1949.',
    '',
    '## Ledger',
    '',
    'Columns are the §7 fields — URL · target keyword · content type · publish date ·',
    'spine pointer · request id — plus `ref` (✅ = CTA carries `?ref=cf_focusbro_*`;',
    '— = not carried, see the attribution note).',
    '',
    '| URL | Target keyword | Type | Published | Spine pointer | Request id | ref |',
    '|---|---|---|---|---|---|:--:|',
  ];

  const body = rows.map((r) => {
    const path = r.url.replace(/^https:\/\/focusbro\.net/, '') || r.url;
    const published = r.publishDate || '—';
    const refMark = r.instrumented ? '✅' : '—';
    return `| \`${path}\` | ${r.targetKeyword} | ${r.contentType} | ${published} | \`${r.spine}\` | \`${r.requestId}\` | ${refMark} |`;
  });

  return `${[...header, ...body].join('\n')}\n`;
}

async function main() {
  const check = process.argv.includes('--check');
  const guides = await loadGuides();
  const rows = guides.map(toRow).sort((a, b) => naturalCompare(a.slug, b.slug));
  const rendered = renderLedger(rows);

  if (check) {
    const current = existsSync(LEDGER_PATH) ? readFileSync(LEDGER_PATH, 'utf8') : '';
    if (current !== rendered) {
      console.error(
        'content-ledger: docs/content-ledger.md is stale. Run `node scripts/generate-content-ledger.mjs` and commit.',
      );
      process.exit(1);
    }
    console.log(`content-ledger: up to date (${rows.length} guides).`);
    return;
  }

  writeFileSync(LEDGER_PATH, rendered);
  const instrumented = rows.filter((r) => r.instrumented).length;
  console.log(
    `content-ledger: wrote docs/content-ledger.md — ${rows.length} guides, ${instrumented} instrumented, ${rows.length - instrumented} gap.`,
  );
}

main();
